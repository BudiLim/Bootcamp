// Write a code to find factorial of a number
// ○ Example: 4! → 4 x 3 x 2 x 1 = 24
// ○ Example: 6! → 6 x 4 x 3 x 2 x 1 = 720


let kali = 4;
let hasilKali = 1;
for (let i = 1; i <= kali; i++){
    hasilKali *= i;
}

console.log(hasilKali)


